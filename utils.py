def add(a, b):
    """
    This function adds two numbers.
    """
    return a + b

def sub(a, b):
    """
    This function subtracts two numbers.
    """
    return a - b

def mul(a, b):
    """
    This function multiplies two numbers.
    """
    return a * b

def div(a, b):
    """
    This function divides two numbers.
    """
    return a / b

def pow(a, b):
    """
    This function raises a number to the power of another number.
    """
    return a ** b
